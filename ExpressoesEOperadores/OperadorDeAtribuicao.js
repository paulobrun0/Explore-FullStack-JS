let value;

// Operador atribuição
value = 10;
console.log(value);

// Incremento
value += 10;
console.log(value);

// Decremento
value -= 2;
console.log(value);

// Multiplicação
value *= 2;
console.log(value);

// Divisão
value /= 2;
console.log(value);

// Resto da divisão
value %= 2;
console.log(value);

// Exponencial
value **= 2;
console.log(value);
