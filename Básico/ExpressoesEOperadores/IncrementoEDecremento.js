let number = 10;

// Incremento
// number = number + 1;
number++;

// Incrementa após (por isso não mostra no console)
console.log(number++);
console.log(number);

// Incrementa antes
console.log(++number);

// Decremento
console.log("Decremento após", number--);
console.log(number);
console.log("Decremento antes", --number);

// Incrementar mais de um (colocar)
number += 10;
console.log(number);

// Decrementar mais de um (remover)
number -= 2;
console.log(number);
