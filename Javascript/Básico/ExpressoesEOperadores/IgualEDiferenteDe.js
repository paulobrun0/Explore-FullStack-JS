let one = 1;
let two = 2;

// == igual a
console.log("### IGUAL A ###");

console.log(one == two);
console.log(one == 1);
console.log(one === "1");

console.log("### IGUAL A ###");

// != diferente de
console.log("### DIFERENTE DE ###");
console.log(one != two);
console.log(one != 1);
console.log(one !== "1");

console.log("### DIFERENTE DE ###");
